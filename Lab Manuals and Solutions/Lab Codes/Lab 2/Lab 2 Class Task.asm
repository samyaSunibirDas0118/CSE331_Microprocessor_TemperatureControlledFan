; LAB 2 TASK: 3 SINGLE-KEY INPUT, OUTPUT

;DIRECTIVES
.MODEL SMALL
.STACK 100H
.DATA
.CODE

; TAKING SINGLE KEY INPUT 3 TIMES         
INPUT:
MOV AH, 1H ; SINGLE KEY INPUT FUNCTION
INT 21H
MOV BL, AL ; 1ST INPUT IN BL

INT 21H
MOV BH, AL ; 2ND INPUT IN BH

INT 21H
MOV CL, AL ; 3RD INPUT IN CL

; NEWLINE  
NEWLINE:
MOV AH, 2H ; SINGLE KEY INPUT FUNCTION
MOV DL,0AH ; NEW LINE
INT 21H
MOV DL,0DH ; CARRIAGE RETURN
INT 21H

; OUTPUT: 2ND INPUT
OUTPUT:
MOV AH, 2H
MOV DL, BH 
INT 21H 

EXIT:
MOV AH, 4CH ; TERMINATING FUNCTION
INT 21H

